package Homework5.homework5task2;

public class Calculator2 {

    public double x;
    public double y;
    public char act;
    public double z;

    public void getMultiplication() {
        if (act == '*')
            System.out.println(z = x * y);
        return;
    }

    public void getDivision() {
        if (act == '/')
            System.out.println(z = x / y);
        return;
    }

    public void getAddition() {
        if (act == '+')
            System.out.println(z = x + y);
        return;
    }

    public void getSubstraction() {
        if (act == '-')
            System.out.println(z = x - y);
        return;
    }
}





